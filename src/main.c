/* ───────────────────────── headless dump (tshark-like) ────────────────── */
static void dump_tree(cfield_t *n, int depth)
{
  cfield_t *c;
  for (c = n->children; c; c = c->next) {
    printf("%*s%s\n", depth * 2, "", c->label[0] ? c->label : c->abbrev);
    dump_tree(c, depth + 1);
  }
}

static int dump_mode(const char *path, const char *expr)
{
  capture_t cap;
  char err[256] = "";
  cfilter_t *flt = NULL;
  long i, shown = 0;
  int printed_detail = 0;

  load_decoders();
  /* Optional port binding for scripted testing, e.g. CARACAL_BIND="udp 69 TFTP" */
  {
    const char *b = getenv("CARACAL_BIND");
    char tr[16], pr[64]; unsigned pt;
    if (b && sscanf(b, "%15s %u %63s", tr, &pt, pr) == 3) {
      add_port_rule(tr, pt, pr);
    }
  }
  if (capture_load(path, &cap, err, sizeof err) != 0) {
    fprintf(stderr, "caracal: %s\n", err);
    return 1;
  }
  if (expr && *expr) {
    flt = filter_compile(expr, err, sizeof err);
    if (!flt) { fprintf(stderr, "caracal: filter: %s\n", err); capture_free(&cap); return 1; }
  }
  printf("# %s — %ld packets, %d custom protocol(s) loaded\n",
         path, cap.count, posa_count());
  if (expr && *expr) printf("# filter: %s\n", expr);

  for (i = 0; i < cap.count; i++) {
    cpkt_t *p = &cap.pkts[i];
    cfield_t *root = dissect_packet(p);
    if (!flt || filter_eval(flt, root)) {
      double t = (double)((p->ts_us >= cap.first_ts_us) ? p->ts_us - cap.first_ts_us : 0) / 1e6;
      dissect_summarize(p);
      printf("%5ld  %.6f  %-15s %-15s %-8s %5u  %s\n",
             i + 1, t, p->col_src, p->col_dst, p->col_proto, p->origlen, p->col_info);
      shown++;
      if (!printed_detail) {
        printf("       └─ detail:\n");
        dump_tree(root, 4);
        printed_detail = 1;
      }
    }
    cfield_free(root);
  }
  printf("# %ld packet(s) matched\n", shown);

  /* Test hook: CARACAL_SAVE=<path> writes the matched packets as pcapng. */
  {
    const char *sp = getenv("CARACAL_SAVE");
    if (sp && *sp) {
      uint8_t *keep = flt ? calloc((size_t)cap.count, 1) : NULL;
      long n;
      if (flt) {
        for (i = 0; i < cap.count; i++) {
          cfield_t *r = dissect_packet(&cap.pkts[i]);
          if (filter_eval(flt, r)) keep[i] = 1;
          cfield_free(r);
        }
      }
      n = capture_save(&cap, sp, keep, err, sizeof err);
      free(keep);
      if (n < 0) fprintf(stderr, "caracal: save: %s\n", err);
      else       printf("# saved %ld packet(s) to %s\n", n, sp);
    }
  }

  if (flt) filter_free(flt);
  capture_free(&cap);
  return 0;
}

/* ───────────────────────── lua mode (generalized MQS) ─────────────────── */
static void apply_bind(const char *spec)   /* "udp 69 TFTP" */
{
  char tr[16], pr[64]; unsigned pt;
  if (sscanf(spec, "%15s %u %63s", tr, &pt, pr) != 3) {
    fprintf(stderr, "caracal: bad -X binding '%s' (want '<udp|tcp> <port> <Proto>')\n", spec);
    return;
  }
  if (strcmp(tr, "udp") && strcmp(tr, "tcp")) {
    fprintf(stderr, "caracal: -X transport must be udp or tcp\n"); return;
  }
  add_port_rule(tr, pt, pr);
}

static int lua_cli(int argc, char **argv)
{
  const char *script = NULL, *capf = NULL, *filt = NULL;
  const char *binds[32]; int nbinds = 0;
  const char *posas[32]; int nposas = 0;
  capture_t cap;
  cfilter_t *flt = NULL;
  char err[256] = "";
  int i, rc;

  for (i = 1; i < argc; i++) {
    if      ((!strcmp(argv[i], "-s") || !strcmp(argv[i], "--script")) && i + 1 < argc) script = argv[++i];
    else if (!strcmp(argv[i], "-r") && i + 1 < argc) capf = argv[++i];
    else if (!strcmp(argv[i], "-f") && i + 1 < argc) filt = argv[++i];
    else if (!strcmp(argv[i], "-X") && i + 1 < argc) { if (nbinds < 32) binds[nbinds++] = argv[++i]; }
    else if (!strcmp(argv[i], "-p") && i + 1 < argc) { if (nposas < 32) posas[nposas++] = argv[++i]; }
    else if (argv[i][0] != '-' && !capf) capf = argv[i];
  }

  if (!script) { fprintf(stderr, "caracal: -s <script.lua> is required\n"); return 2; }
  if (!capf)   { fprintf(stderr, "caracal: -r <capture> is required\n"); return 2; }

  load_decoders();
  for (i = 0; i < nposas; i++) {
    if (posa_load_file(posas[i], err, sizeof err) < 0)
      fprintf(stderr, "caracal: -p %s: %s\n", posas[i], err);
  }
  for (i = 0; i < nbinds; i++) apply_bind(binds[i]);

  if (capture_load(capf, &cap, err, sizeof err) != 0) {
    fprintf(stderr, "caracal: %s\n", err);
    return 1;
  }
  if (filt && *filt) {
    flt = filter_compile(filt, err, sizeof err);
    if (!flt) { fprintf(stderr, "caracal: filter: %s\n", err); capture_free(&cap); return 1; }
  }

  rc = caracal_lua_run(script, &cap, flt, err, sizeof err);
  if (rc != 0) fprintf(stderr, "caracal: lua: %s\n", err);

  if (flt) filter_free(flt);
  capture_free(&cap);
  return rc ? 1 : 0;
}

static int has_arg(int argc, char **argv, const char *a, const char *b)
{
  int i;
  for (i = 1; i < argc; i++)
    if (!strcmp(argv[i], a) || (b && !strcmp(argv[i], b))) return 1;
  return 0;
}

/* ───────────────────────── main ───────────────────────────────────────── */
int main(int argc, char **argv)
{
  gtcaca_application_widget_t *appw;
  gtcaca_window_widget_t *win;
  int W, H;
  int int_w;
  int widths[7];

  app.detail_for = -1;

  /* Headless mode for scripting/testing: caracal --dump <file> [filter] */
  if (argc >= 3 && strcmp(argv[1], "--dump") == 0)
    return dump_mode(argv[2], argc > 3 ? argv[3] : NULL);

  /* Lua scripting mode (generalized MQS): caracal -s script.lua -r cap … */
  if (has_arg(argc, argv, "-s", "--script"))
    return lua_cli(argc, argv);

  if (gtcaca_init(&argc, &argv) < 0) {
    fprintf(stderr, "caracal: cannot initialize display\n");
    return 1;
  }

  /* Load bundled protocol definitions (best effort). */
  load_decoders();

  W = caca_get_canvas_width(gmo.cv);
  H = caca_get_canvas_height(gmo.cv);

  appw = gtcaca_application_new("caracal");
  win  = gtcaca_window_new(GTCACA_WIDGET(appw), NULL, 0, 0, W, H);

  build_menu();

  gtcaca_label_new(GTCACA_WIDGET(win), "Filter:", 0, 1);
  if (W >= 60) {
    int gw = 18;                       /* filter-match gauge at the right */
    app.fentry = gtcaca_entry_new(GTCACA_WIDGET(win), 8, 1, W - 9 - gw - 1);
    app.matchbar = gtcaca_gauge_new(GTCACA_WIDGET(win), W - gw, 1, gw);
    gtcaca_gauge_set_colors(app.matchbar, CACA_GREEN, CACA_BLUE);
    gtcaca_gauge_set_value(app.matchbar, 1.0f);
  } else {
    app.fentry = gtcaca_entry_new(GTCACA_WIDGET(win), 8, 1, W - 9);
  }

  app.table_y = 2;
  app.table_h = (H - 4) * 55 / 100;
  if (app.table_h < 4) app.table_h = 4;
  app.tree_y  = app.table_y + app.table_h;
  app.tree_h  = H - 1 - app.tree_y;
  if (app.tree_h < 3) app.tree_h = 3;

  app.table = gtcaca_table_new(GTCACA_WIDGET(win), 0, app.table_y, W, app.table_h);
  gtcaca_table_set_model(app.table, &table_model);
  gtcaca_table_set_title(app.table, "Packets");

  int_w = W - 2;
  widths[0] = 7;  widths[1] = 12; widths[2] = 18; widths[3] = 18;
  widths[4] = 9;  widths[5] = 7;
  widths[6] = int_w - (widths[0]+widths[1]+widths[2]+widths[3]+widths[4]+widths[5]);
  if (widths[6] < 6) widths[6] = 6;
  gtcaca_table_set_column_widths(app.table, widths, 7);

  /* Lower area is split: detail tree on the left, hex byte pane on the right. */
  app.tree_w = W * 60 / 100;
  if (app.tree_w < 20) app.tree_w = W;
  app.tree = gtcaca_tree_new(GTCACA_WIDGET(win), 0, app.tree_y, app.tree_w, app.tree_h);
  gtcaca_tree_set_model(app.tree, &tree_model);
  gtcaca_tree_set_title(app.tree, "Packet details");

  if (app.tree_w < W) {
    app.hex = gtcaca_hexview_new(GTCACA_WIDGET(win), app.tree_w, app.tree_y,
                                 W - app.tree_w, app.tree_h);
    gtcaca_hexview_set_title(app.hex, "Bytes");
  }

  app.bar = gtcaca_statusbar_new("");

  /* both detail panes shown by default; relayout sizes everything */
  app.show_tree = 1;
  app.show_hex  = (app.hex != NULL);
  relayout();
  update_view_labels();

  /* Open a file passed on the command line. Optional: --graph <ip|port> opens
     the Entity Explorer rooted at that entity once the capture is loaded. */
  {
    const char *capfile = NULL, *graph_entity = NULL;
    int ai;
    for (ai = 1; ai < argc; ai++) {
      if (!strcmp(argv[ai], "--graph") && ai + 1 < argc) graph_entity = argv[++ai];
      else if (argv[ai][0] != '-' && !capfile) capfile = argv[ai];
    }
    if (capfile) load_capture(capfile);
    else app.filter = filter_compile("", NULL, 0);
    g_pending_graph = graph_entity;
  }

  app.focus = FOCUS_TABLE;

  /* ── event loop ──────────────────────────────────────────────────────── */
  caca_set_mouse(gmo.dp, 1);
  redraw();
  if (g_pending_graph && g_pending_graph[0]) {   /* --graph <ip|port> */
    int isport = 1, j;
    for (j = 0; g_pending_graph[j]; j++)
      if (!isdigit((unsigned char)g_pending_graph[j])) { isport = 0; break; }
    if (isport) explorer_run(1, NULL, atoi(g_pending_graph));
    else        explorer_run(0, g_pending_graph, 0);
    redraw();
  }
  while (!g_quit) {
    caca_event_t ev;
    int t, key;

    if (app.capturing) {
      /* Pump the capture ring without blocking the UI, then poll for input. */
      long before = app.cap.count;
      int nd = pcapng_capture_dispatch(app.cap_handle, 64, capture_cb, NULL);
      if (nd < 0) { stop_capture(); gtcaca_dialog_message("Capture", "capture ended (device error)"); }
      if (app.cap.count != before) {
        if (app.cap_follow && app.table && app.nview > 0)
          gtcaca_table_set_current(app.table, app.nview - 1, 0);
        redraw();
      }
      if (!caca_get_event(gmo.dp, CACA_EVENT_ANY, &ev, 0)) { usleep(15000); continue; }
    } else {
      if (!caca_get_event(gmo.dp, CACA_EVENT_ANY, &ev, -1)) continue;
    }
    t = caca_get_event_type(&ev);

    if (t & CACA_EVENT_RESIZE) { redraw(); continue; }

    if (t & CACA_EVENT_MOUSE_PRESS) {
      int b  = caca_get_event_mouse_button(&ev);
      int mx = caca_get_mouse_x(gmo.dp);
      int my = caca_get_mouse_y(gmo.dp);
      if (b == 4 || b == 5) {                       /* wheel → scroll focused */
        int k = (b == 4) ? CACA_KEY_DOWN : CACA_KEY_UP;
        if (app.focus == FOCUS_TREE)              gtcaca_tree_key(app.tree, k, NULL);
        else if (app.focus == FOCUS_HEX && app.hex) gtcaca_hexview_key(app.hex, k, NULL);
        else                                      gtcaca_table_key(app.table, k, NULL);
      } else if (b == 1) {                          /* left click → focus pane */
        if (my == 1)                         app.focus = FOCUS_FILTER;
        else if (my >= app.tree_y && app.hex && mx >= app.tree_w) app.focus = FOCUS_HEX;
        else if (my >= app.tree_y)           app.focus = FOCUS_TREE;
        else if (my >= app.table_y) {
          long row = app.table->top + (my - (app.table_y + 3));
          app.focus = FOCUS_TABLE;
          if (row >= 0 && row < app.nview) app.table->sel = row;
        }
      }
      redraw();
      continue;
    }

    if (!(t & CACA_EVENT_KEY_PRESS)) continue;
    key = caca_get_event_key_ch(&ev);

    /* menu owns input while open */
    if (app.menu->has_focus) {
      if (key == CACA_KEY_ESCAPE) { app.menu->has_focus = 0; app.menu->is_open = 0; }
      else gtcaca_menu_handle_key(app.menu, key);
      redraw();
      continue;
    }

    /* global keys */
    if (key == CACA_KEY_F10) { app.menu->has_focus = 1; app.menu->active_entry = 0; app.menu->is_open = 0; redraw(); continue; }
    if (key == CACA_KEY_F2)  { act_open(NULL); redraw(); continue; }
    if (key == CTRL('q'))    { if (confirm_quit()) break; redraw(); continue; }
    if (key == CTRL('o'))    { act_open(NULL); redraw(); continue; }
    if (key == CTRL('s'))    { act_save(NULL); redraw(); continue; }
    if (key == CTRL('f'))    { act_find(NULL); redraw(); continue; }
    if (key == '\t') {
      app.focus = (app.focus + 1) % FOCUS_COUNT;
      if (app.focus == FOCUS_HEX && !app.hex) app.focus = FOCUS_FILTER;
      redraw();
      continue;
    }

    if (app.focus == FOCUS_FILTER) {
      if (key == CACA_KEY_RETURN || key == 10) {
        apply_filter(gtcaca_entry_get_text(app.fentry));
        app.focus = FOCUS_TABLE;
      } else if (key == CACA_KEY_ESCAPE) {
        app.focus = FOCUS_TABLE;
      } else {
        filter_entry_key(key);
      }
      redraw();
      continue;
    }

    /* '/' focuses the filter from a table/tree pane */
    if (key == '/') { app.focus = FOCUS_FILTER; redraw(); continue; }
    /* n / N step through find matches (when a search is active) */
    if (app.find_active && (key == 'n' || key == 'N')) {
      find_jump(key == 'n' ? +1 : -1); redraw(); continue;
    }
    if (key == 'q' || key == 'Q') { if (confirm_quit()) break; redraw(); continue; }

    if      (app.focus == FOCUS_TABLE) gtcaca_table_key(app.table, key, NULL);
    else if (app.focus == FOCUS_HEX && app.hex) gtcaca_hexview_key(app.hex, key, NULL);
    else                               gtcaca_tree_key(app.tree, key, NULL);
    redraw();
  }

  caca_set_mouse(gmo.dp, 0);
  stop_capture();
  detail_clear();
  if (app.filter) filter_free(app.filter);
  capture_free(&app.cap);
  free(app.view);
  caca_free_display(gmo.dp);
  return 0;
}
