/* scroll a textview until Esc/q */
static void modal_scroll_loop(gtcaca_textview_widget_t *tv)
{
  caca_event_t ev;
  tv->has_focus = 1;
  for (;;) {
    int k;
    gtcaca_redraw();
    if (!caca_get_event(gmo.dp, CACA_EVENT_KEY_PRESS, &ev, -1)) continue;
    k = caca_get_event_key_ch(&ev);
    if (k == CACA_KEY_ESCAPE || k == 'q') break;
    if (tv->private_key_cb) tv->private_key_cb(tv, k, NULL);
  }
}

static void tv_destroy(gtcaca_textview_widget_t *tv)
{ gtcaca_textview_clear(tv); CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(tv)); free(tv); }

/* append `data` as printable text, wrapped, each line carrying `prefix`. */
static void tv_block(gtcaca_textview_widget_t *tv, const char *prefix,
                     const uint8_t *d, int len, int width)
{
  char line[600];
  int i = 0, avail = width - (int)strlen(prefix) - 1;
  if (avail < 8) avail = 8;
  if (avail > (int)sizeof line - 1) avail = (int)sizeof line - 1;
  if (len <= 0) return;
  while (i < len) {
    int o = (int)snprintf(line, sizeof line, "%s", prefix), j;
    for (j = 0; j < avail && i < len; j++, i++) {
      uint8_t b = d[i];
      line[o++] = (b >= 32 && b < 127) ? (char)b : '.';
    }
    line[o] = '\0';
    gtcaca_textview_append(tv, line);
  }
}

/* ── Follow TCP / UDP stream ─────────────────────────────────────────────── */
typedef struct { gtcaca_textview_widget_t *tv; uint32_t cip; uint16_t cport; int width; int any; } follow_ctx_t;

static void follow_tcp_cb(void *ud, uint32_t sip, uint16_t sport, uint32_t dip,
                          uint16_t dport, int dir, const uint8_t *data, size_t len,
                          const uint8_t *all, size_t alllen)
{
  follow_ctx_t *f = ud;
  const char *pfx = (sip == f->cip && sport == f->cport) ? "C: " : "S: ";
  (void)dip; (void)dport; (void)dir; (void)all; (void)alllen;
  tv_block(f->tv, pfx, data, (int)len, f->width);
  f->any = 1;
}

static int same_flow(const caracal_l4_t *a, const caracal_l4_t *b)
{
  return (a->sip == b->sip && a->dip == b->dip && a->sport == b->sport && a->dport == b->dport) ||
         (a->sip == b->dip && a->dip == b->sip && a->sport == b->dport && a->dport == b->sport);
}

static void act_follow_tcp(void *u)
{
  long sel = app.table ? gtcaca_table_selected_row(app.table) : -1;
  long pidx = view_pkt_index(sel), i;
  caracal_l4_t l4;
  gtcaca_window_widget_t *win;
  gtcaca_textview_widget_t *tv;
  pcapng_tcp_reasm_t *ctx;
  follow_ctx_t fc;
  int w, h;
  (void)u;

  if (pidx < 0 || !caracal_locate_l4(&app.cap.pkts[pidx], &l4) || l4.proto != 6) {
    gtcaca_dialog_message("Follow TCP Stream", "Select a TCP packet first."); return;
  }
  win = modal_window("Follow TCP Stream", &w, &h);
  tv  = gtcaca_textview_new(GTCACA_WIDGET(win), 1, 1, w - 2, h - 2);
  ctx = pcapng_tcp_reasm_new();
  fc.tv = tv; fc.cip = l4.sip; fc.cport = l4.sport; fc.width = w - 2; fc.any = 0;

  { long nseg = 0;
    for (i = 0; i < app.cap.count; i++) {
      caracal_l4_t a;
      cpkt_t *p = &app.cap.pkts[i];
      if (!caracal_locate_l4(p, &a) || a.proto != 6 || !same_flow(&a, &l4)) continue;
      nseg++;
      pcapng_tcp_reasm_add(ctx, a.sip, a.dip, a.sport, a.dport, a.seq, a.flags,
                           a.paylen > 0 ? p->data + a.payoff : NULL,
                           (size_t)(a.paylen > 0 ? a.paylen : 0), follow_tcp_cb, &fc);
    }
    pcapng_tcp_reasm_free(ctx);
    if (!fc.any) {
      char msg[256];
      snprintf(msg, sizeof msg,
        "Port %u \xe2\x86\x94 %u: %ld segment(s) in this stream, but none carry payload.",
        l4.sport, l4.dport, nseg);
      gtcaca_textview_append(tv, msg);
      gtcaca_textview_append(tv, "");
      gtcaca_textview_append(tv, "This TCP stream has only ACK/control packets (Len=0) \xe2\x80\x94");
      gtcaca_textview_append(tv, "the data segments were not captured, or there is no data.");
      gtcaca_textview_append(tv, "Follow a packet whose Len>0 (e.g. an Application Data packet).");
    }
  }
  modal_scroll_loop(tv);
  tv_destroy(tv);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(win)); free(win);
  modal_close_menu();
}

static void act_follow_udp(void *u)
{
  long sel = app.table ? gtcaca_table_selected_row(app.table) : -1;
  long pidx = view_pkt_index(sel), i;
  caracal_l4_t l4;
  gtcaca_window_widget_t *win;
  gtcaca_textview_widget_t *tv;
  int w, h, any = 0;
  (void)u;

  if (pidx < 0 || !caracal_locate_l4(&app.cap.pkts[pidx], &l4) || l4.proto != 17) {
    gtcaca_dialog_message("Follow UDP Stream", "Select a UDP packet first."); return;
  }
  win = modal_window("Follow UDP Stream", &w, &h);
  tv  = gtcaca_textview_new(GTCACA_WIDGET(win), 1, 1, w - 2, h - 2);
  for (i = 0; i < app.cap.count; i++) {
    caracal_l4_t a;
    cpkt_t *p = &app.cap.pkts[i];
    if (!caracal_locate_l4(p, &a) || a.proto != 17 || !same_flow(&a, &l4)) continue;
    if (a.paylen <= 0) continue;
    tv_block(tv, (a.sip == l4.sip && a.sport == l4.sport) ? "C: " : "S: ",
             p->data + a.payoff, a.paylen, w - 2);
    any = 1;
  }
  if (!any) gtcaca_textview_append(tv, "(no datagram payload)");
  modal_scroll_loop(tv);
  tv_destroy(tv);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(win)); free(win);
  modal_close_menu();
}

/* ── IO Graph (Wireshark-style: multiple graphs, filters, colours, styles) ─ */
#define IOG_MAX 8

/* Y-axis modes (cf. Wireshark). Field modes aggregate a chosen field's value. */
enum {
  IOG_Y_PACKETS = 0, IOG_Y_BYTES, IOG_Y_BITS,
  IOG_Y_SUM, IOG_Y_COUNT_FRAMES, IOG_Y_COUNT_FIELDS,
  IOG_Y_MAX, IOG_Y_MIN, IOG_Y_AVG, IOG_Y_NMODES
};
#define IOG_Y_ISFIELD(y) ((y) >= IOG_Y_SUM)

typedef struct {
  int        enabled;
  char       name[40];
  char       filter_text[128];
  cfilter_t *filter;              /* compiled; NULL = match all           */
  uint8_t    color;
  int        style;               /* GTCACA_LC_*                          */
  int        yunit;               /* IOG_Y_*                              */
  char       yfield[64];          /* field abbrev for the field Y modes   */
} iog_series_t;

static const double IOG_INTERVALS[] = { 0.001, 0.01, 0.1, 1.0, 10.0, 60.0 };
#define IOG_NINTERVALS ((int)(sizeof IOG_INTERVALS / sizeof IOG_INTERVALS[0]))
static const uint8_t IOG_PALETTE[] = {
  CACA_GREEN, CACA_YELLOW, CACA_LIGHTRED, CACA_LIGHTCYAN,
  CACA_LIGHTMAGENTA, CACA_WHITE, CACA_LIGHTBLUE, CACA_BROWN };
#define IOG_NPAL ((int)(sizeof IOG_PALETTE / sizeof IOG_PALETTE[0]))

static int _pal_idx(uint8_t c)
{ int i; for (i = 0; i < IOG_NPAL; i++) if (IOG_PALETTE[i] == c) return i; return 0; }

static const char *iog_color_name(uint8_t c)
{
  switch (c) {
  case CACA_GREEN: return "Green";   case CACA_YELLOW: return "Yellow";
  case CACA_LIGHTRED: return "Red";  case CACA_LIGHTCYAN: return "Cyan";
  case CACA_LIGHTMAGENTA: return "Magenta"; case CACA_WHITE: return "White";
  case CACA_LIGHTBLUE: return "Blue"; case CACA_BROWN: return "Orange";
  default: return "?";
  }
}
static const char *iog_style_name(int s)
{ static const char *n[] = { "Line", "Impulse", "Bar", "Dot" }; return n[s & 3]; }

/* Human description of a series' Y axis, e.g. "Bytes" or "AVG(tcp.window_size)". */
static void iog_yaxis_desc(const iog_series_t *s, char *out, size_t sz)
{
  const char *fld = s->yfield[0] ? s->yfield : "?";
  switch (s->yunit) {
  case IOG_Y_PACKETS:      snprintf(out, sz, "Packets"); break;
  case IOG_Y_BYTES:        snprintf(out, sz, "Bytes");   break;
  case IOG_Y_BITS:         snprintf(out, sz, "Bits");    break;
  case IOG_Y_SUM:          snprintf(out, sz, "SUM(%s)", fld); break;
  case IOG_Y_COUNT_FRAMES: snprintf(out, sz, "COUNT FRAMES(%s)", fld); break;
  case IOG_Y_COUNT_FIELDS: snprintf(out, sz, "COUNT FIELDS(%s)", fld); break;
  case IOG_Y_MAX:          snprintf(out, sz, "MAX(%s)", fld); break;
  case IOG_Y_MIN:          snprintf(out, sz, "MIN(%s)", fld); break;
  case IOG_Y_AVG:          snprintf(out, sz, "AVG(%s)", fld); break;
  default:                 snprintf(out, sz, "?"); break;
  }
}

/* Collect a field's numeric occurrences from a dissection. */
static void iog_field_stats(cfield_t *root, const char *abbrev, int *nfields,
                            int *nnum, double *sum, double *mn, double *mx)
{
  cfield_t *out[256];
  int n = cfield_collect(root, abbrev, out, 256), i, num = 0;
  double s = 0, lo = 0, hi = 0;
  for (i = 0; i < n; i++) {
    double v;
    if (out[i]->vtype == CV_UINT) v = (double)out[i]->u;
    else continue;                          /* only numeric fields aggregate */
    if (num == 0) { lo = hi = v; } else { if (v < lo) lo = v; if (v > hi) hi = v; }
    s += v; num++;
  }
  *nfields = n; *nnum = num; *sum = s; *mn = lo; *mx = hi;
}

/* Recompute every series' bins over the capture and (re)load the chart + list. */
static void iog_refresh(gtcaca_linechart_widget_t *lc, gtcaca_textlist_widget_t *tl,
                        iog_series_t *ser, int nser, int sel,
                        uint64_t t0, uint64_t span_us, double interval, int log_y)
{
  int nb = (int)(span_us / (interval * 1e6)) + 1;
  double **acc, **cnt;             /* acc = value, cnt = #samples (for AVG) */
  int s;
  long i;
  int need_dissect = 0;

  if (nb < 1) nb = 1; if (nb > 4000) nb = 4000;
  acc = calloc((size_t)nser, sizeof *acc);
  cnt = calloc((size_t)nser, sizeof *cnt);
  for (s = 0; s < nser; s++) { acc[s] = calloc((size_t)nb, sizeof **acc);
                               cnt[s] = calloc((size_t)nb, sizeof **cnt); }
  for (s = 0; s < nser; s++)
    if (ser[s].enabled && (ser[s].filter || IOG_Y_ISFIELD(ser[s].yunit))) need_dissect = 1;

  for (i = 0; i < app.cap.count; i++) {
    cpkt_t *p = &app.cap.pkts[i];
    int b = (int)((double)(p->ts_us - t0) / (interval * 1e6));
    cfield_t *root = NULL;
    if (b < 0) b = 0; if (b >= nb) b = nb - 1;
    if (need_dissect) root = dissect_packet(p);
    for (s = 0; s < nser; s++) {
      iog_series_t *g = &ser[s];
      if (!g->enabled) continue;
      if (g->filter && !(root && filter_eval(g->filter, root))) continue;
      switch (g->yunit) {
      case IOG_Y_PACKETS: acc[s][b] += 1.0; break;
      case IOG_Y_BYTES:   acc[s][b] += (double)p->origlen; break;
      case IOG_Y_BITS:    acc[s][b] += (double)p->origlen * 8.0; break;
      default: {          /* field-based modes */
        int nf, nn; double sum, mn, mx;
        if (!root || !g->yfield[0]) break;
        iog_field_stats(root, g->yfield, &nf, &nn, &sum, &mn, &mx);
        if (g->yunit == IOG_Y_COUNT_FRAMES) { if (nf > 0) acc[s][b] += 1.0; }
        else if (g->yunit == IOG_Y_COUNT_FIELDS) acc[s][b] += (double)nf;
        else if (nn > 0) {
          if (g->yunit == IOG_Y_SUM) acc[s][b] += sum;
          else if (g->yunit == IOG_Y_AVG) { acc[s][b] += sum; cnt[s][b] += nn; }
          else if (g->yunit == IOG_Y_MAX) { if (cnt[s][b] == 0 || mx > acc[s][b]) acc[s][b] = mx; cnt[s][b] = 1; }
          else if (g->yunit == IOG_Y_MIN) { if (cnt[s][b] == 0 || mn < acc[s][b]) acc[s][b] = mn; cnt[s][b] = 1; }
        }
        break; }
      }
    }
    if (root) cfield_free(root);
  }

  /* finalize AVG (value / sample count) */
  for (s = 0; s < nser; s++)
    if (ser[s].yunit == IOG_Y_AVG)
      for (i = 0; i < nb; i++) if (cnt[s][i] > 0) acc[s][i] /= cnt[s][i];

  gtcaca_linechart_clear(lc);
  gtcaca_linechart_set_log_y(lc, log_y);
  gtcaca_linechart_set_xspan(lc, (double)span_us / 1e6, "s");
  for (s = 0; s < nser; s++)
    if (ser[s].enabled)
      gtcaca_linechart_add_series_styled(lc, acc[s], nb, ser[s].color, ser[s].style, ser[s].name);

  /* config list rows */
  gtcaca_textlist_clear(tl);
  for (s = 0; s < nser; s++) {
    char row[256], ydesc[80];
    iog_yaxis_desc(&ser[s], ydesc, sizeof ydesc);
    snprintf(row, sizeof row, "%s %-13.13s %-8.8s %-8.8s %-18.18s %s",
             ser[s].enabled ? "[x]" : "[ ]", ser[s].name,
             iog_color_name(ser[s].color), iog_style_name(ser[s].style),
             ydesc,
             ser[s].filter_text[0] ? ser[s].filter_text : "(all packets)");
    gtcaca_textlist_append(tl, row);
  }
  if (sel >= 0 && sel < nser) tl->selected_item = (unsigned)sel;

  for (s = 0; s < nser; s++) { free(acc[s]); free(cnt[s]); }
  free(acc); free(cnt);
}

static void act_io_graph(void *u)
{
  gtcaca_window_widget_t *win;
  gtcaca_linechart_widget_t *lc;
  gtcaca_textlist_widget_t *tl;
  gtcaca_label_widget_t *hdr, *help;
  iog_series_t ser[IOG_MAX];
  int nser = 0, w, h, ch, iv = 3, log_y = 0, s;
  uint64_t t0, t1, span_us;
  long i;
  char title[160];
  caca_event_t ev;
  (void)u;

  if (app.cap.count == 0) { gtcaca_dialog_message("IO Graph", "No capture loaded."); return; }

  t0 = t1 = app.cap.pkts[0].ts_us;
  for (i = 0; i < app.cap.count; i++) {
    uint64_t t = app.cap.pkts[i].ts_us;
    if (t > t1) t1 = t; if (t < t0) t0 = t;
  }
  span_us = (t1 > t0) ? (t1 - t0) : 1;
  /* pick a default interval aiming for ~100 bins */
  { double want = (double)span_us / 1e6 / 100.0; int best = 3; double bd = 1e18;
    for (s = 0; s < IOG_NINTERVALS; s++) { double d = IOG_INTERVALS[s] > want ?
        IOG_INTERVALS[s] - want : want - IOG_INTERVALS[s];
      if (d < bd) { bd = d; best = s; } }
    iv = best; }

  /* default graphs: all packets, plus the active display filter if any */
  memset(ser, 0, sizeof ser);
  ser[0].enabled = 1; snprintf(ser[0].name, sizeof ser[0].name, "All packets");
  ser[0].filter = NULL; ser[0].color = IOG_PALETTE[0]; ser[0].style = GTCACA_LC_LINE; ser[0].yunit = 0;
  nser = 1;
  if (app.filter_text[0] && app.filter) {
    char err[128];
    ser[1].enabled = 1;
    snprintf(ser[1].name, sizeof ser[1].name, "Filter");
    snprintf(ser[1].filter_text, sizeof ser[1].filter_text, "%s", app.filter_text);
    ser[1].filter = filter_compile(app.filter_text, err, sizeof err);
    ser[1].color = IOG_PALETTE[1]; ser[1].style = GTCACA_LC_IMPULSE; ser[1].yunit = 0;
    nser = 2;
  }

  win = modal_window("IO Graph", &w, &h);
  ch  = (h - 4) * 55 / 100; if (ch < 6) ch = 6; if (ch > h - 8) ch = h - 8;
  lc  = gtcaca_linechart_new(GTCACA_WIDGET(win), 1, 1, w - 2, ch);
  hdr = gtcaca_label_new(GTCACA_WIDGET(win),
          "  Graph          Color     Style    Y-Axis   Display filter", 2, ch + 1);
  hdr->width = w - 4;
  tl  = gtcaca_textlist_new(GTCACA_WIDGET(win), 2, ch + 2);
  gtcaca_textlist_widget_set_view_size(tl, h - ch - 5 > 1 ? h - ch - 5 : 1);
  help = gtcaca_label_new(GTCACA_WIDGET(win),
          "space on/off  f filter  F field  n name  c color  t style  y axis  i interval  L log  a add  d del  Esc", 2, h - 2);
  help->width = w - 4;

  snprintf(title, sizeof title, "IO Graph  \xe2\x80\x94  interval %g s%s", IOG_INTERVALS[iv], log_y ? "  log-Y" : "");
  gtcaca_linechart_set_title(lc, title);
  iog_refresh(lc, tl, ser, nser, 0, t0, span_us, IOG_INTERVALS[iv], log_y);
  tl->has_focus = 1;

  for (;;) {
    int k, sel;
    gtcaca_redraw();
    if (!caca_get_event(gmo.dp, CACA_EVENT_KEY_PRESS, &ev, -1)) continue;
    k = caca_get_event_key_ch(&ev);
    sel = (int)tl->selected_item;
    if (sel < 0) sel = 0; if (sel >= nser) sel = nser - 1;

    if (k == CACA_KEY_ESCAPE || k == 'q') break;
    else if (k == CACA_KEY_UP)   { gtcaca_textlist_selection_up(tl);   continue; }
    else if (k == CACA_KEY_DOWN) { gtcaca_textlist_selection_down(tl); continue; }
    else if (k == ' ')  ser[sel].enabled = !ser[sel].enabled;
    else if (k == 'c')  ser[sel].color = IOG_PALETTE[(_pal_idx(ser[sel].color) + 1) % IOG_NPAL];
    else if (k == 't')  ser[sel].style = (ser[sel].style + 1) & 3;
    else if (k == 'y') {
      ser[sel].yunit = (ser[sel].yunit + 1) % IOG_Y_NMODES;
      /* entering a field mode with no field yet → prompt for one */
      if (IOG_Y_ISFIELD(ser[sel].yunit) && !ser[sel].yfield[0]) {
        char fld[64] = "";
        if (prompt_line("Y field (e.g. tcp.window_size):  ", fld, sizeof fld) && fld[0])
          snprintf(ser[sel].yfield, sizeof ser[sel].yfield, "%s", fld);
      }
    }
    else if (k == 'F') {
      char fld[64];
      snprintf(fld, sizeof fld, "%s", ser[sel].yfield);
      if (prompt_line("Y field (e.g. tcp.window_size):  ", fld, sizeof fld))
        snprintf(ser[sel].yfield, sizeof ser[sel].yfield, "%s", fld);
    }
    else if (k == 'i')  iv = (iv + 1) % IOG_NINTERVALS;
    else if (k == 'L')  log_y = !log_y;
    else if (k == 'n') {
      char nm[40];
      snprintf(nm, sizeof nm, "%s", ser[sel].name);
      if (prompt_line("Graph name:  ", nm, sizeof nm)) snprintf(ser[sel].name, sizeof ser[sel].name, "%s", nm);
    }
    else if (k == 'f') {
      char fx[128], err[128];
      snprintf(fx, sizeof fx, "%s", ser[sel].filter_text);
      if (prompt_line("Display filter:  ", fx, sizeof fx)) {
        cfilter_t *nf = fx[0] ? filter_compile(fx, err, sizeof err) : NULL;
        if (fx[0] && !nf) gtcaca_dialog_message("Filter error", err[0] ? err : "invalid filter");
        else {
          if (ser[sel].filter) filter_free(ser[sel].filter);
          ser[sel].filter = nf;
          snprintf(ser[sel].filter_text, sizeof ser[sel].filter_text, "%s", fx);
        }
      }
    }
    else if (k == 'a' && nser < IOG_MAX) {
      memset(&ser[nser], 0, sizeof ser[nser]);
      ser[nser].enabled = 1;
      snprintf(ser[nser].name, sizeof ser[nser].name, "Graph %d", nser + 1);
      ser[nser].color = IOG_PALETTE[nser % IOG_NPAL];
      ser[nser].style = GTCACA_LC_LINE; ser[nser].yunit = 0;
      sel = nser; nser++;
    }
    else if (k == 'd' && nser > 1) {
      if (ser[sel].filter) filter_free(ser[sel].filter);
      for (s = sel; s < nser - 1; s++) ser[s] = ser[s + 1];
      nser--; if (sel >= nser) sel = nser - 1;
    }
    else { if (tl->private_key_cb) tl->private_key_cb(tl, k, NULL); continue; }

    snprintf(title, sizeof title, "IO Graph  \xe2\x80\x94  interval %g s%s", IOG_INTERVALS[iv], log_y ? "  log-Y" : "");
    gtcaca_linechart_set_title(lc, title);
    iog_refresh(lc, tl, ser, nser, sel, t0, span_us, IOG_INTERVALS[iv], log_y);
  }

  for (s = 0; s < nser; s++) if (ser[s].filter) filter_free(ser[s].filter);
  gtcaca_textlist_clear(tl);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(help)); free(help);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(tl));   free(tl);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(hdr));  free(hdr);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(lc));   gtcaca_linechart_free(lc);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(win));  free(win);
  modal_close_menu();
}

/* ── Protocol Hierarchy statistics (cf. Wireshark) ──────────────────────── */
typedef struct phnode {
  char name[32];
  long pkts, bytes, end_pkts, end_bytes;
  struct phnode *child, *sibling;
} phnode_t;

static phnode_t *g_ph_root;
static long      g_ph_total_pkts;

static phnode_t *ph_child(phnode_t *parent, const char *name)
{
  phnode_t *c, *last = NULL;
  for (c = parent->child; c; c = c->sibling) { if (!strcmp(c->name, name)) return c; last = c; }
  c = calloc(1, sizeof *c);
  if (!c) return NULL;
  snprintf(c->name, sizeof c->name, "%s", name);
  if (last) last->sibling = c; else parent->child = c;
  return c;
}
static void ph_free(phnode_t *n)
{
  phnode_t *c, *nx;
  if (!n) return;
  for (c = n->child; c; c = nx) { nx = c->sibling; ph_free(c); }
  free(n);
}
static const char *ph_disp(const char *a)
{
  if (!strcmp(a, "frame")) return "Frame";
  if (!strcmp(a, "eth"))   return "Ethernet";
  if (!strcmp(a, "ip"))    return "Internet Protocol v4";
  if (!strcmp(a, "ipv6"))  return "Internet Protocol v6";
  if (!strcmp(a, "udp"))   return "User Datagram Protocol";
  if (!strcmp(a, "tcp"))   return "Transmission Control Protocol";
  if (!strcmp(a, "ntp"))   return "Network Time Protocol";
  if (!strcmp(a, "dns"))   return "Domain Name System";
  if (!strcmp(a, "data"))  return "Data";
  return a;
}

/* current entry resolver for the tree model */
static phnode_t *ph_at(phnode_t *node, int i)
{
  phnode_t *c = node ? node->child : (g_ph_root ? g_ph_root->child : NULL);
  for (; c && i > 0; c = c->sibling) i--;
  return c;
}
static long ph_count(phnode_t *node)
{
  phnode_t *c = node ? node->child : (g_ph_root ? g_ph_root->child : NULL);
  long n = 0;
  for (; c; c = c->sibling) n++;
  return n;
}

/* A shared progressbar widget, drawn by the cell renderer at each row's rect
   (kept out of the global draw list — we paint it ourselves per row). */
static gtcaca_progressbar_widget_t *g_ph_bar;

static long phm_cc(gtcaca_tree_model_t *m, void *node) { (void)m; return ph_count(node); }
static void *phm_child(gtcaca_tree_model_t *m, void *node, long i) { (void)m; return ph_at(node, (int)i); }
static int  phm_has(gtcaca_tree_model_t *m, void *node) { phnode_t *n = node; (void)m; return n && n->child != NULL; }
static void phm_label(gtcaca_tree_model_t *m, void *node, char *b, int len)
{ phnode_t *n = node; (void)m; snprintf(b, len, "%s", ph_disp(n->name)); }

/* Cell renderer: name + counts on the left, a real gtcaca progressbar (percent
   of packets) on the right — a widget living inside the tree row. */
static void ph_draw_row(gtcaca_tree_model_t *m, void *node, int x, int y, int width, int selected)
{
  phnode_t *n = node;
  double pp = g_ph_total_pkts ? (double)n->pkts / (double)g_ph_total_pkts : 0.0;
  char left[120];
  int barw = width * 42 / 100, leftw;
  (void)m; (void)selected;
  if (barw < 14) barw = 14;
  if (barw > width - 6) barw = width > 6 ? width - 6 : 0;
  leftw = width - barw - 1; if (leftw < 4) leftw = 4;
  snprintf(left, sizeof left, "%s  %ld pk %ld B", ph_disp(n->name), n->pkts, n->bytes);
  if ((int)strlen(left) > leftw) left[leftw] = '\0';
  caca_put_str(gmo.cv, x, y, left);           /* inherits the row colour set by the tree */
  if (g_ph_bar && barw >= 8) {
    g_ph_bar->x = x + leftw + 1; g_ph_bar->y = y; g_ph_bar->width = barw;
    gtcaca_progressbar_set_value(g_ph_bar, (float)pp);
    gtcaca_progressbar_draw(g_ph_bar);
  }
}
static gtcaca_tree_model_t ph_model = { phm_cc, phm_child, phm_label, phm_has, NULL, ph_draw_row };

/* Protocol Hierarchy: an expandable tree whose rows host a real progressbar
   widget via the tree's cell-renderer delegate. */
static void act_proto_stats(void *u)
{
  gtcaca_window_widget_t *win;
  gtcaca_tree_widget_t *tree;
  int w, h;
  long k;
  caca_event_t ev;
  (void)u;

  if (app.nview == 0) { gtcaca_dialog_message("Protocol Hierarchy", "No packets to summarise."); return; }

  g_ph_root = calloc(1, sizeof *g_ph_root);
  g_ph_total_pkts = app.nview;
  for (k = 0; k < app.nview; k++) {
    cpkt_t *p = &app.cap.pkts[view_pkt_index(k)];
    cfield_t *root = dissect_packet(p);
    phnode_t *cur = g_ph_root, *last = NULL;
    int j, nc;
    if (!root) continue;
    nc = cfield_count(root);
    for (j = 0; j < nc; j++) {
      cfield_t *ch = cfield_child_at(root, j);
      phnode_t *pn;
      if (!ch->abbrev[0] || ch->vtype != CV_NONE) continue;   /* protocol layers only */
      pn = ph_child(cur, ch->abbrev);
      if (!pn) break;
      pn->pkts++;
      pn->bytes += ch->len > 0 ? ch->len : 0;
      cur = pn; last = pn;
    }
    if (last) { last->end_pkts++; last->end_bytes += p->origlen; }
    cfield_free(root);
  }

  win = modal_window("Protocol Hierarchy Statistics", &w, &h);
  /* a standalone progressbar the cell renderer repositions per row */
  g_ph_bar = gtcaca_progressbar_new(NULL, 0, 0, 10);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(g_ph_bar));
  tree = gtcaca_tree_new(GTCACA_WIDGET(win), 1, 1, w - 2, h - 2);
  gtcaca_tree_set_model(tree, &ph_model);
  gtcaca_tree_set_title(tree, "Protocol  (Right/Enter expand, Esc close)");
  tree->has_focus = 1;
  for (;;) {
    int key;
    gtcaca_redraw();
    if (!caca_get_event(gmo.dp, CACA_EVENT_KEY_PRESS, &ev, -1)) continue;
    key = caca_get_event_key_ch(&ev);
    if (key == CACA_KEY_ESCAPE || key == 'q') break;
    gtcaca_tree_key(tree, key, NULL);
  }
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(tree)); gtcaca_tree_free(tree);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(win)); free(win);
  free(g_ph_bar); g_ph_bar = NULL;
  ph_free(g_ph_root); g_ph_root = NULL;
  modal_close_menu();
}

/* ══ NetFlow / Conversations ════════════════════════════════════════════════
 * Aggregates packets into conversations (flows) keyed by protocol + endpoint
 * pair, then presents them two ways: a Host → Conversation tree with a packet
 * table for the selected flow, and a mind-map for browsing. Enter on a packet
 * jumps to it in the main list. */
typedef struct {
  char     proto[8];
  char     ipa[48]; int porta;
  char     ipb[48]; int portb;
  long    *pk; long npk, cpk;    /* packet indices in this flow             */
  long     bytes;
  uint64_t first_us, last_us;
} nf_flow_t;

typedef struct {
  char  ip[48];
  long  npkts;
  int  *fl; int nfl, cfl;        /* flow indices this host participates in   */
  int   first_conv, nconv;       /* slice into convnodes                     */
} nf_host_t;

typedef struct { int kind; int host; int flow; } nf_node_t;   /* 0=host 1=conv */

typedef struct {
  nf_flow_t *flows; int nflows, cflows;
  nf_host_t *hosts; int nhosts, chosts;
  nf_node_t *hostnodes;
  nf_node_t *convnodes; int nconv;
  int selflow;                   /* flow whose packets the table shows       */
} nf_ctx_t;

static nf_ctx_t *g_nf;

static void nf_ep(const char *ip, int port, char *out, size_t n)
{
  if (port <= 0)            snprintf(out, n, "%s", ip);
  else if (strchr(ip, ':')) snprintf(out, n, "[%s]:%d", ip, port);   /* IPv6 */
  else                      snprintf(out, n, "%s:%d", ip, port);
}

static nf_flow_t *nf_find_flow(nf_ctx_t *c, const char *proto,
                               const char *ipa, int pa, const char *ipb, int pb)
{
  int i;
  for (i = 0; i < c->nflows; i++) {
    nf_flow_t *f = &c->flows[i];
    if (f->porta == pa && f->portb == pb && !strcmp(f->proto, proto) &&
        !strcmp(f->ipa, ipa) && !strcmp(f->ipb, ipb)) return f;
  }
  if (c->nflows == c->cflows) {
    int nc = c->cflows ? c->cflows * 2 : 32;
    c->flows = realloc(c->flows, (size_t)nc * sizeof *c->flows); c->cflows = nc;
  }
  { nf_flow_t *f = &c->flows[c->nflows++];
    memset(f, 0, sizeof *f);
    snprintf(f->proto, sizeof f->proto, "%s", proto);
    snprintf(f->ipa, sizeof f->ipa, "%s", ipa); f->porta = pa;
    snprintf(f->ipb, sizeof f->ipb, "%s", ipb); f->portb = pb;
    return f; }
}

static nf_host_t *nf_find_host(nf_ctx_t *c, const char *ip)
{
  int i;
  for (i = 0; i < c->nhosts; i++) if (!strcmp(c->hosts[i].ip, ip)) return &c->hosts[i];
  if (c->nhosts == c->chosts) {
    int nc = c->chosts ? c->chosts * 2 : 32;
    c->hosts = realloc(c->hosts, (size_t)nc * sizeof *c->hosts); c->chosts = nc;
  }
  { nf_host_t *h = &c->hosts[c->nhosts++];
    memset(h, 0, sizeof *h);
    snprintf(h->ip, sizeof h->ip, "%s", ip);
    return h; }
}

static void nf_host_add_flow(nf_host_t *h, int flowidx)
{
  int j;
  for (j = 0; j < h->nfl; j++) if (h->fl[j] == flowidx) return;
  if (h->nfl == h->cfl) { int nc = h->cfl ? h->cfl * 2 : 8;
    h->fl = realloc(h->fl, (size_t)nc * sizeof *h->fl); h->cfl = nc; }
  h->fl[h->nfl++] = flowidx;
}

static nf_ctx_t *nf_build(void)
{
  nf_ctx_t *c = calloc(1, sizeof *c);
  long k; int i, idx;
  c->selflow = -1;
  for (k = 0; k < app.nview; k++) {
    long pidx = view_pkt_index(k);
    cpkt_t *p = &app.cap.pkts[pidx];
    caracal_l4_t l4;
    int has, pa, pb, swap, hostflow;
    const char *proto; char ipa[48], ipb[48];
    nf_flow_t *f;
    if (pidx < 0) continue;
    dissect_summarize(p);
    if (!p->col_src[0] || !p->col_dst[0]) continue;
    has = caracal_locate_l4(p, &l4) && (l4.proto == 6 || l4.proto == 17);
    proto = has ? (l4.proto == 6 ? "TCP" : "UDP") : (p->col_proto[0] ? p->col_proto : "?");
    snprintf(ipa, sizeof ipa, "%s", p->col_src); pa = has ? l4.sport : 0;
    snprintf(ipb, sizeof ipb, "%s", p->col_dst); pb = has ? l4.dport : 0;
    swap = strcmp(ipa, ipb); if (swap == 0) swap = pa - pb;
    if (swap > 0) { char t[48]; int tp; memcpy(t, ipa, 48); memcpy(ipa, ipb, 48); memcpy(ipb, t, 48);
                    tp = pa; pa = pb; pb = tp; }
    f = nf_find_flow(c, proto, ipa, pa, ipb, pb);
    if (f->npk == f->cpk) { long nc = f->cpk ? f->cpk * 2 : 16;
      f->pk = realloc(f->pk, (size_t)nc * sizeof *f->pk); f->cpk = nc; }
    f->pk[f->npk++] = pidx;
    f->bytes += p->origlen;
    if (f->npk == 1) f->first_us = f->last_us = p->ts_us;
    else { if (p->ts_us < f->first_us) f->first_us = p->ts_us;
           if (p->ts_us > f->last_us)  f->last_us  = p->ts_us; }
    hostflow = (int)(f - c->flows);
    { nf_host_t *ha = nf_find_host(c, ipa); ha->npkts++; nf_host_add_flow(ha, hostflow); }
    { nf_host_t *hb = nf_find_host(c, ipb); hb->npkts++; nf_host_add_flow(hb, hostflow); }
  }
  /* node arrays for the tree */
  c->hostnodes = malloc((size_t)(c->nhosts ? c->nhosts : 1) * sizeof *c->hostnodes);
  for (i = 0; i < c->nhosts; i++) { c->hostnodes[i].kind = 0; c->hostnodes[i].host = i; c->hostnodes[i].flow = -1; }
  for (i = 0, c->nconv = 0; i < c->nhosts; i++) c->nconv += c->hosts[i].nfl;
  c->convnodes = malloc((size_t)(c->nconv ? c->nconv : 1) * sizeof *c->convnodes);
  for (i = 0, idx = 0; i < c->nhosts; i++) {
    int j; c->hosts[i].first_conv = idx; c->hosts[i].nconv = c->hosts[i].nfl;
    for (j = 0; j < c->hosts[i].nfl; j++) {
      c->convnodes[idx].kind = 1; c->convnodes[idx].host = i; c->convnodes[idx].flow = c->hosts[i].fl[j]; idx++;
    }
  }
  return c;
}

static void nf_free(nf_ctx_t *c)
{
  int i;
  if (!c) return;
  for (i = 0; i < c->nflows; i++) free(c->flows[i].pk);
  for (i = 0; i < c->nhosts; i++) free(c->hosts[i].fl);
  free(c->flows); free(c->hosts); free(c->hostnodes); free(c->convnodes); free(c);
}

/* other endpoint of a conversation relative to a host, formatted */
static void nf_conv_other(const nf_flow_t *f, const char *host_ip, char *out, size_t n)
{
  if (!strcmp(host_ip, f->ipa)) nf_ep(f->ipb, f->portb, out, n);
  else                          nf_ep(f->ipa, f->porta, out, n);
}

/* ── NetFlow tree model (host → conversation) ───────────────────────────── */
static long nf_tcc(gtcaca_tree_model_t *m, void *node)
{ nf_node_t *n = node; (void)m;
  if (!n) return g_nf->nhosts;
  return (n->kind == 0) ? g_nf->hosts[n->host].nconv : 0; }
static void *nf_tchild(gtcaca_tree_model_t *m, void *node, long i)
{ nf_node_t *n = node; (void)m;
  if (!n) return &g_nf->hostnodes[i];
  return &g_nf->convnodes[g_nf->hosts[n->host].first_conv + i]; }
static int nf_thas(gtcaca_tree_model_t *m, void *node)
{ nf_node_t *n = node; (void)m; return n && n->kind == 0 && g_nf->hosts[n->host].nconv > 0; }
static void nf_tlabel(gtcaca_tree_model_t *m, void *node, char *b, int l)
{
  nf_node_t *n = node; (void)m;
  if (!n) { b[0] = '\0'; return; }
  if (n->kind == 0) {
    nf_host_t *h = &g_nf->hosts[n->host];
    snprintf(b, l, "%s   (%ld pkts, %d flows)", h->ip, h->npkts, h->nfl);
  } else {
    nf_flow_t *f = &g_nf->flows[n->flow];
    char other[64];
    nf_conv_other(f, g_nf->hosts[n->host].ip, other, sizeof other);
    snprintf(b, l, "%-4s \xe2\x86\x94 %-28s  %ld pkts  %ld B", f->proto, other, f->npk, f->bytes);
  }
}
static gtcaca_tree_model_t nf_tree_model = { nf_tcc, nf_tchild, nf_tlabel, nf_thas, NULL, NULL };

/* ── NetFlow packet sub-table (packets of the selected flow) ────────────── */
static long nfp_rows(gtcaca_table_model_t *m)
{ (void)m; return (g_nf && g_nf->selflow >= 0) ? g_nf->flows[g_nf->selflow].npk : 0; }
static int  nfp_cols(gtcaca_table_model_t *m) { (void)m; return 7; }
static void nfp_cell(gtcaca_table_model_t *m, long row, int col, char *b, int l)
{
  nf_flow_t *f; long pidx; cpkt_t *p;
  (void)m;
  if (!g_nf || g_nf->selflow < 0) { b[0] = '\0'; return; }
  f = &g_nf->flows[g_nf->selflow];
  if (row < 0 || row >= f->npk) { b[0] = '\0'; return; }
  pidx = f->pk[row]; p = &app.cap.pkts[pidx];
  dissect_summarize(p);
  switch (col) {
  case 0: snprintf(b, l, "%ld", pidx + 1); break;
  case 1: { double t = (double)((p->ts_us >= app.cap.first_ts_us) ? p->ts_us - app.cap.first_ts_us : 0) / 1e6;
            snprintf(b, l, "%.6f", t); break; }
  case 2: snprintf(b, l, "%s", p->col_src); break;
  case 3: snprintf(b, l, "%s", p->col_dst); break;
  case 4: snprintf(b, l, "%s", p->col_proto); break;
  case 5: snprintf(b, l, "%u", p->origlen); break;
  case 6: snprintf(b, l, "%s", p->col_info); break;
  default: b[0] = '\0';
  }
}
static gtcaca_table_model_t nf_pkt_model = { nfp_rows, nfp_cols, tm_header, nfp_cell, NULL };

/* Populate the mind-map: Capture → hosts → conversations. */
static void nf_fill_mindmap(gtcaca_mindmap_widget_t *mm)
{
  gtcaca_mm_node_t *root;
  int i, j;
  gtcaca_mindmap_clear(mm, "Capture");
  root = gtcaca_mindmap_root(mm);
  for (i = 0; i < g_nf->nhosts; i++) {
    nf_host_t *h = &g_nf->hosts[i];
    char t[80];
    gtcaca_mm_node_t *hn;
    snprintf(t, sizeof t, "%s (%ld)", h->ip, h->npkts);
    hn = gtcaca_mindmap_add_child(mm, root, t);
    for (j = 0; j < h->nfl; j++) {
      nf_flow_t *f = &g_nf->flows[h->fl[j]];
      char other[64], ct[100];
      gtcaca_mm_node_t *cn;
      nf_conv_other(f, h->ip, other, sizeof other);
      snprintf(ct, sizeof ct, "%s \xe2\x86\x94 %s (%ld)", f->proto, other, f->npk);
      cn = gtcaca_mindmap_add_child(mm, hn, ct);
      if (cn) cn->userdata = f;               /* map map-node → flow */
    }
    if (hn) hn->folded = 1;                    /* start collapsed for browsing */
  }
}

enum { NFV_LIST = 0, NFV_MAP = 1 };

static void act_netflow(void *u)
{
  gtcaca_window_widget_t *win;
  gtcaca_tree_widget_t *tree;
  gtcaca_table_widget_t *tbl;
  gtcaca_mindmap_widget_t *mm;
  gtcaca_label_widget_t *help;
  int w, h, th, mode = NFV_LIST, focus_tbl = 0, prev_flow = -2;
  long jump = -1, r;
  caca_event_t ev;
  (void)u;

  if (app.nview == 0) { gtcaca_dialog_message("NetFlow", "No packets to summarise."); return; }
  g_nf = nf_build();
  if (g_nf->nflows == 0) { nf_free(g_nf); g_nf = NULL;
    gtcaca_dialog_message("NetFlow", "No conversations found."); modal_close_menu(); return; }
  g_nf->selflow = (g_nf->nconv > 0) ? g_nf->convnodes[0].flow : (g_nf->nflows ? 0 : -1);

  win = modal_window("NetFlow \xe2\x80\x94 Conversations", &w, &h);
  th  = (h - 3) * 45 / 100; if (th < 4) th = 4; if (th > h - 6) th = h - 6;
  tree = gtcaca_tree_new(GTCACA_WIDGET(win), 1, 1, w - 2, th);
  gtcaca_tree_set_model(tree, &nf_tree_model);
  gtcaca_tree_set_title(tree, "Hosts \xe2\x96\xb8 Conversations");
  tbl  = gtcaca_table_new(GTCACA_WIDGET(win), 1, 1 + th, w - 2, h - 3 - th);
  gtcaca_table_set_model(tbl, &nf_pkt_model);
  mm   = gtcaca_mindmap_new(GTCACA_WIDGET(win), 1, 1, w - 2, h - 3);
  gtcaca_mindmap_set_title(mm, "Connections map");
  nf_fill_mindmap(mm);
  mm->is_visible = 0;
  help = gtcaca_label_new(GTCACA_WIDGET(win), "", 2, h - 2);
  help->width = w - 4;

  for (;;) {
    int k, sel_flow;
    nf_node_t *tn;
    /* keep the packet table bound to the tree's selected conversation */
    tn = (nf_node_t *)gtcaca_tree_selected_node(tree);
    if (mode == NFV_LIST && tn && tn->kind == 1) g_nf->selflow = tn->flow;
    if (g_nf->selflow != prev_flow) { gtcaca_table_set_current(tbl, 0, 0); prev_flow = g_nf->selflow; }

    tree->is_visible = tbl->is_visible = (mode == NFV_LIST);
    mm->is_visible = (mode == NFV_MAP);
    tree->has_focus = (mode == NFV_LIST && !focus_tbl);
    tbl->has_focus  = (mode == NFV_LIST &&  focus_tbl);
    mm->has_focus   = (mode == NFV_MAP);
    help->label = (char *)(mode == NFV_MAP
      ? "  \xe2\x86\x91\xe2\x86\x93 browse   \xe2\x86\x90/\xe2\x86\x92 fold   Enter view packets   m list   Esc close"
      : focus_tbl
        ? "  \xe2\x86\x91\xe2\x86\x93 packets   Enter jump to packet   Tab tree   m map   Esc close"
        : "  \xe2\x86\x91\xe2\x86\x93/\xe2\x86\x92 tree   Tab packets   m map   Esc close");

    gtcaca_redraw();
    if (!caca_get_event(gmo.dp, CACA_EVENT_KEY_PRESS, &ev, -1)) continue;
    k = caca_get_event_key_ch(&ev);
    if (k == CACA_KEY_ESCAPE || k == 'q') break;
    if (k == 'm') { mode = (mode == NFV_LIST) ? NFV_MAP : NFV_LIST; continue; }

    if (mode == NFV_MAP) {
      if (k == CACA_KEY_RETURN || k == 10) {
        gtcaca_mm_node_t *n = gtcaca_mindmap_selected(mm);
        if (n && n->userdata) {
          g_nf->selflow = (int)((nf_flow_t *)n->userdata - g_nf->flows);
          mode = NFV_LIST; focus_tbl = 1;
        }
      } else gtcaca_mindmap_key(mm, k, NULL);
      continue;
    }

    /* list mode */
    if (k == '\t') { focus_tbl = !focus_tbl; continue; }
    if (focus_tbl) {
      if (k == CACA_KEY_RETURN || k == 10) {
        sel_flow = g_nf->selflow;
        if (sel_flow >= 0) {
          long row = gtcaca_table_selected_row(tbl);
          if (row >= 0 && row < g_nf->flows[sel_flow].npk) { jump = g_nf->flows[sel_flow].pk[row]; break; }
        }
      } else gtcaca_table_key(tbl, k, NULL);
    } else {
      gtcaca_tree_key(tree, k, NULL);
    }
  }

  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(help)); free(help);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(mm));   gtcaca_mindmap_free(mm);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(tbl));  free(tbl);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(tree)); gtcaca_tree_free(tree);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(win));  free(win);
  nf_free(g_nf); g_nf = NULL;
  modal_close_menu();

  /* jump to the chosen packet in the main list */
  if (jump >= 0)
    for (r = 0; r < app.nview; r++)
      if (view_pkt_index(r) == jump) {
        gtcaca_table_set_current(app.table, r, 0);
        detail_rebuild(r);
        break;
      }
}

/* ══ Entity Explorer (Maltego-style pivoting over connections) ══════════════
 * Start from an IP or a port and expand its connections on demand; re-root to
 * pivot on any node, and pull the matching packets into the main view. Built on
 * the mind-map widget with lazy expansion driven by the NetFlow flow model. */
typedef struct { int kind; char ip[48]; int port; int expanded; } expl_ent_t; /* 0=IP 1=PORT */
typedef struct { expl_ent_t **v; int n, c; } expl_pool_t;

static expl_ent_t *expl_ent_new(expl_pool_t *pool, int kind, const char *ip, int port)
{
  expl_ent_t *e = calloc(1, sizeof *e);
  e->kind = kind; e->port = port;
  if (ip) snprintf(e->ip, sizeof e->ip, "%s", ip);
  if (pool->n == pool->c) { int nc = pool->c ? pool->c * 2 : 64;
    pool->v = realloc(pool->v, (size_t)nc * sizeof *pool->v); pool->c = nc; }
  pool->v[pool->n++] = e;
  return e;
}
static void expl_pool_free(expl_pool_t *pool)
{ int i; for (i = 0; i < pool->n; i++) free(pool->v[i]); free(pool->v); }

static void expl_ent_label(const expl_ent_t *e, char *out, size_t n)
{ if (e->kind == 1) snprintf(out, n, "port %d", e->port); else snprintf(out, n, "%s", e->ip); }

/* Expand an entity node: add a child per connection touching it. */
static void expl_expand(gtcaca_mindmap_widget_t *mm, gtcaca_mm_node_t *node, expl_pool_t *pool)
{
  expl_ent_t *e = node->userdata;
  int i, added = 0;
  if (!e) return;
  if (e->expanded) { gtcaca_mindmap_toggle_fold(node); return; }
  e->expanded = 1; node->folded = 0;
  for (i = 0; i < g_nf->nflows; i++) {
    nf_flow_t *f = &g_nf->flows[i];
    gtcaca_mm_node_t *cn; char lbl[120];
    if (e->kind == 0) {                         /* IP → each conversation */
      int a = !strcmp(f->ipa, e->ip), b = !strcmp(f->ipb, e->ip);
      const char *peer; int pport; char ep[64];
      if (!a && !b) continue;
      peer = a ? f->ipb : f->ipa; pport = a ? f->portb : f->porta;
      nf_ep(peer, pport, ep, sizeof ep);
      snprintf(lbl, sizeof lbl, "%-4s \xe2\x86\x92 %-28s  %ld pkts  %ld B", f->proto, ep, f->npk, f->bytes);
      cn = gtcaca_mindmap_add_child(mm, node, lbl);
      if (cn) cn->userdata = expl_ent_new(pool, 0, peer, 0);
      added++;
    } else {                                    /* PORT → each host using it */
      int a = (f->porta == e->port), b = (f->portb == e->port);
      const char *host;
      if (!a && !b) continue;
      host = a ? f->ipa : f->ipb;
      snprintf(lbl, sizeof lbl, "%-4s  %-30s  %ld pkts", f->proto, host, f->npk);
      cn = gtcaca_mindmap_add_child(mm, node, lbl);
      if (cn) cn->userdata = expl_ent_new(pool, 0, host, 0);
      added++;
    }
  }
  if (!added) { gtcaca_mm_node_t *cn = gtcaca_mindmap_add_child(mm, node, "(no connections)");
                if (cn) cn->userdata = NULL; }
}

static void expl_set_root(gtcaca_mindmap_widget_t *mm, expl_pool_t *pool, expl_ent_t *root)
{
  char lbl[80];
  gtcaca_mm_node_t *r;
  expl_ent_label(root, lbl, sizeof lbl);
  gtcaca_mindmap_clear(mm, lbl);
  r = gtcaca_mindmap_root(mm);
  root->expanded = 0;
  r->userdata = root;
  expl_expand(mm, r, pool);
}

/* Build a display filter that pulls the packets touching an entity. */
static void expl_filter_for(const expl_ent_t *e, char *out, size_t n)
{
  if (e->kind == 1)            snprintf(out, n, "tcp.port == %d || udp.port == %d", e->port, e->port);
  else if (strchr(e->ip, ':')) snprintf(out, n, "ipv6.addr == %s", e->ip);
  else                         snprintf(out, n, "ip.addr == %s", e->ip);
}

static void explorer_run(int kind, const char *ip, int port)
{
  gtcaca_window_widget_t *win;
  gtcaca_mindmap_widget_t *mm;
  gtcaca_label_widget_t *help;
  expl_pool_t pool;
  expl_ent_t *root;
  int w, h;
  char vfilter[160] = "";
  caca_event_t ev;

  if (app.nview == 0) { gtcaca_dialog_message("Entity Explorer", "No packets to explore."); return; }
  g_nf = nf_build();
  memset(&pool, 0, sizeof pool);
  root = expl_ent_new(&pool, kind, ip, port);

  win  = modal_window("Entity Explorer", &w, &h);
  mm   = gtcaca_mindmap_new(GTCACA_WIDGET(win), 1, 1, w - 2, h - 3);
  gtcaca_mindmap_set_title(mm, "Connections");
  help = gtcaca_label_new(GTCACA_WIDGET(win),
           "  \xe2\x86\x91\xe2\x86\x93 nav   Enter expand/collapse   r re-root (pivot)   v view packets   Esc close", 2, h - 2);
  help->width = w - 4;
  expl_set_root(mm, &pool, root);
  mm->has_focus = 1;

  for (;;) {
    int k;
    gtcaca_mm_node_t *sel;
    gtcaca_redraw();
    if (!caca_get_event(gmo.dp, CACA_EVENT_KEY_PRESS, &ev, -1)) continue;
    k = caca_get_event_key_ch(&ev);
    sel = gtcaca_mindmap_selected(mm);
    if (k == CACA_KEY_ESCAPE || k == 'q') break;
    else if (k == CACA_KEY_RETURN || k == 10) { if (sel) expl_expand(mm, sel, &pool); }
    else if (k == 'r') { if (sel && sel->userdata) expl_set_root(mm, &pool, (expl_ent_t *)sel->userdata); }
    else if (k == 'v') { if (sel && sel->userdata) { expl_filter_for((expl_ent_t *)sel->userdata, vfilter, sizeof vfilter); break; } }
    else gtcaca_mindmap_key(mm, k, NULL);
  }

  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(help)); free(help);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(mm));   gtcaca_mindmap_free(mm);
  CDL_DELETE(gmo.widgets_list, GTCACA_WIDGET(win));  free(win);
  expl_pool_free(&pool);
  nf_free(g_nf); g_nf = NULL;
  modal_close_menu();

  if (vfilter[0]) apply_filter(vfilter);   /* pivot the main view to the entity */
}

